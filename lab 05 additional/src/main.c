// –†–µ–∞–ª—ñ–∑—É–≤–∞—Ç–∏ –ø—Ä–æ–≥—Ä–∞–º—É –∑–∞ –¥–æ–ø–æ–º–æ–≥–æ—é —Ç—Ä—å–æ—Ö —Ç–∏–ø—ñ–≤ —Ü–∏–∫–ª—ñ–≤:  for, while-do, do-while (–æ—Ç—Ä–∏–º–∞—Ç–∏ —Ç—Ä–∏ –æ–¥–Ω–∞–∫–æ–≤–∏—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∏)

